package com.hk.master.dto;

public class RakBarcodeDTO {

	private String kode;
	private String id;
	private Integer noUrut;
	private String gdId;
	private String gdNama;
	
	
	public String getKode() {
		return kode;
	}
	public void setKode(String kode) {
		this.kode = kode;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Integer getNoUrut() {
		return noUrut;
	}
	public void setNoUrut(Integer noUrut) {
		this.noUrut = noUrut;
	}
	public String getGdId() {
		return gdId;
	}
	public void setGdId(String gdId) {
		this.gdId = gdId;
	}
	public String getGdNama() {
		return gdNama;
	}
	public void setGdNama(String gdNama) {
		this.gdNama = gdNama;
	}
	
	
	
}
